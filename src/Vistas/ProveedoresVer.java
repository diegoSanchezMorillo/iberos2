    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int fila;
        fila = (Integer)this.jTableProveedores.getSelectedRow();
        int id = (Integer)this.jTableProveedores.getValueAt(fila, 0);
        Proveedores proveedor = new Proveedores();
        proveedor.setIdproveedores(id);
        proveedor = operaciones.seleccionaProveedor(proveedor);
        ProveedoresModificar provMod = new ProveedoresModificar();
        provMod.setVisible(true);
        provMod.jTextFieldNombre.setText(proveedor.getNombre());
        provMod.jTextFieldIdentificacion.setText(proveedor.getIdentificacion());
        provMod.jTextFieldDireccion.setText(proveedor.getDireccion());
        provMod.jTextFieldPoblacion.setText(proveedor.getPoblacion());
        provMod.jTextFieldCodPostal.setText(String.valueOf(proveedor.getCodPostal()));
        provMod.jTextFieldTelefono.setText(String.valueOf(proveedor.getTelefono()));
        provMod.jTextFieldEmail.setText(proveedor.getEmail());
        provMod.id = id;
        this.dispose();
        
    }//GEN-LAST:event_jButton2ActionPerformed

