    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
         String buscar = jTextField1.getText();

        String campo = (String) jComboBox1.getSelectedItem();
        
        List<Proveedores> lista = operaciones.ListaProveedoresBuscar(campo,buscar);
        tabla = (DefaultTableModel) jTableProveedores.getModel();
        if(lista.size()>0){
            tabla.setRowCount(0);
            Iterator consulta = lista.iterator();
            while(consulta.hasNext()){
                Vector datos = new Vector();
                Proveedores fila = (Proveedores)consulta.next();
                datos.add(fila.getIdproveedores());
                datos.add(fila.getNombre());
                datos.add(fila.getIdentificacion());
                datos.add(fila.getPoblacion());               
                datos.add(fila.getTelefono());
                datos.add(fila.getEmail());
                tabla.addRow(datos);
            }
        }else{
            JOptionPane.showMessageDialog(null, "El proveedor no existe"); 
            }
        
        jTableProveedores.getColumnModel().getColumn(0).setMaxWidth(0);
        jTableProveedores.getColumnModel().getColumn(0).setMinWidth(0);
        jTableProveedores.getColumnModel().getColumn(0).setPreferredWidth(0);
    }//GEN-LAST:event_jButton4ActionPerformed

   
